Vector1 <- c(1,2,3)
print(Vector1)

Vector2 <- c(5,6,7)
print(Vector2)

#addition
print(Vector1+Vector2)


#subtraction
print(Vector1-Vector2)


#division
print(Vector1/Vector2)

#sum function
print(sum(Vector1))

#mean function
print(mean(Vector1))

#standard deviation function
print(sd(Vector1))


#product function
print(prod(Vector1))


